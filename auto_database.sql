CREATE TABLE auto(
	name CHAR(20) NOT NULL,
	type CHAR(10) NOT NULL,
	afrom CHAR(20) NOT NULL,
	ato CHAR(20) NOT NULL,
	contact CHAR(10) ,
	email CHAR(20)
);

CREATE TABLE traveller(
	name CHAR(20) NOT NULL,
	gender CHAR(6) ,
	contact CHAR(10) NOT NULL,
	dob CHAR(20) ,
	address CHAR(30) ,
	email CHAR(20)
);


